"""
13. Користувач вводить з клавіатури числа в стовпчик до тих пір, поки не введе число 0. На основі
введених даних потрібно сформувати список, що складається з квадратів введених чисел.
Скорий Євген 141
"""

square = []

print("Введіть числа(якщо хочите завершити дію введіть 0)")
while True:
    number = int(input(": "))
    if number == 0:
        break
    else:
        square.append(number ** 2)


print("Квадрати введених чисел - ", square)